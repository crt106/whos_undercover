function getRandomWordPair() {
  const pair = wordPairs[Math.floor(Math.random() * wordPairs.length)];
  // 随机决定哪个是平民词、哪个是卧底词
  if (Math.random() > 0.5) {
    return { civilianWord: pair[0], undercoverWord: pair[1] };
  }
  return { civilianWord: pair[1], undercoverWord: pair[0] };
}

